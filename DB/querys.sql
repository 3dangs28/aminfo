CREATE TABLE SERVICIOS(
  ID_SERVICIO int NOT NULL AUTO_INCREMENT PRIMARY KEY,
  ID_USUARIO INT NOT NULL,
  NOMBRE VARCHAR(30),
  APELLIDO VARCHAR(30),
  CEDULA VARCHAR(30),
  DIRECCION VARCHAR(30),
  CORREO VARCHAR(25),
  DESCRIPCION  TEXT,
